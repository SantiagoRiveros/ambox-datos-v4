# OPERADORES DE COMPARACION o COMPARATIVOS

# Mayor que
print(10 > 5)  # Esto da True, porque es verdadero

# Mayor igual que
print(10 >= 10)  # Estp da True

# Menor que
print(3 < 1)  # Esto da False, porque no es verdareo

# Menor igual que
print(2 <= 1)  # Esto da false

# Igual
print(2 == 2)  # Esto da True

# Desigual
print(2 != "Hola")  # True


# OPERADORES LOGICOS

# Tenemos 3(Basicos), sirven para usar mas de una comparacion (por ejemplo)
# and, or y not

# operador and, devuelve verdadero, si las dos condiciones, son verdaderas
# operador and = &
print(True & True)  # Esto devuelve True, porque las dos condiciones son verdaderas
print(True & False)  # False
print(False & True)  # False
print(False & False)  # False


# operador OR, devuelve verdadero, cuando cualquiera de las dos condiciones, es verdadera
# operador OR = |
print(True | True)  # Devuelve True
print(True | False)  # True
print(False | True)  # True
print(False | False)  # False

# operador not, invierte el estado de veracidad
print(not True)  # False
print(not False)  # True
